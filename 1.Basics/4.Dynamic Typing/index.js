// JavaScript is a Dynamic Language it means 
// that the type of variables can be changed dynamically
// eg.

let name ='Vedant';
console.log(typeof name)

name = 1;
console.log(typeof name);

name = 3.1;
console.log(typeof name)

name = true;
console.log(typeof name);

name = null;
console.log(typeof name);

name = undefined;
console.log(typeof name);
